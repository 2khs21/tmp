#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*num;

	if (n == -2147483648)
	{
		num = "-2147483648";
		ft_putstr_fd(num, fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd(n % 10 + '0', fd);
}
